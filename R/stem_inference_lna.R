                        do_prevalence        = do_prevalence,
                        step_size            = step_size,
                        tparam_ess           = tparam_ess
                  )
            }
            
            # Save the latent process if called for in this iteration
            if ((iter-1) %% thin_latent_proc == 0) {
                  ess_record[, param_rec_ind - 1] <- path$ess_record    # save the ESS record
                  mat_2_arr(lna_paths, path$lna_path, path_rec_ind - 1) # save the path
                  mat_2_arr(lna_draws, path$draws, path_rec_ind - 1)    # save the N(0,1) draws
                  path_rec_ind <- path_rec_ind + 1                      # increment the path record index
            }
            
            # Save the parameters if called for in this iteration
            if ((iter-1) %% thin_params == 0) {
                  
                  # Save the lna log likelihood, data log likelihood, and log priors
                  data_log_lik[param_rec_ind]     <- path$data_log_lik
                  lna_log_lik[param_rec_ind]      <- sum(dnorm(path$draws, log = T))
                  params_log_prior[param_rec_ind] <- params_logprior_cur
                  
                  if (!fixed_inits)
                        initdist_log_prior[param_rec_ind] <- initdist_prior(initdist_params_cur)
                  if (!t0_fixed)
                        t0_log_prior[param_rec_ind] <- t0_logprior_cur
                  
                  if (!is.null(tparam)) {
                        
                        if (tparam_update == "block") tparam_ess_record[param_rec_ind] <- tparam_ess
                        
                        for (p in seq_along(tparam)) tparam[[p]]$log_lik <- sum(dnorm(tparam[[p]]$draws_cur, log = T))
                        
                        tparam_log_lik[param_rec_ind, ] <- sapply(tparam, "[[", "log_lik")
                        mat_2_arr(tparam_samples, lna_params_cur[, tparam_inds + 1, drop = FALSE], param_rec_ind - 1)
                  }
                  
                  # Store the parameter sample
                  parameter_samples_nat[param_rec_ind,] <- c(model_params_nat, t0, initdist_params_cur)
                  parameter_samples_est[param_rec_ind,] <- c(model_params_est, t0, init_volumes_cur)
                  
                  # Store the proposal covariance matrix if monitoring is requested
                  if (mcmc_kernel$method == "c_rw_adaptive") {
                        adaptation_scale_record[, param_rec_ind] <- proposal_scaling
                        adaptation_shape_record[, , param_rec_ind] <-
                              kernel_cov
                        
                        if (messages &&
                            iter < stop_adaptation)
                              cat(
                                    paste0("Iteration: ", iter),
                                    paste0(
                                          "Componentwise acceptances: ",
                                          paste0(acceptances_c, collapse = ", ")
                                    ),
                                    paste0(
                                          "Acceptance rates: ",
                                          paste0(acceptances_c / (iter - 1), collapse = ",")
                                    ),
                                    file = status_file,
                                    sep = "\n",
                                    append = TRUE
                              )
                        
                  } else if (mcmc_kernel$method == "mvn_c_adaptive") {
                        adaptation_scale_record[, param_rec_ind] <- proposal_scaling
                        adaptation_shape_record[, , param_rec_ind] <-
                              kernel_cov
                        
                        if (messages &&
                            iter < stop_adaptation)
                              cat(
                                    paste0("Iteration: ", iter),
                                    paste0("Global acceptances: ", acceptances_g),
                                    paste0(
                                          "Global acceptance rate: ",
                                          acceptances_g / (iter - 1)
                                    ),
                                    file = status_file,
                                    sep = "\n",
                                    append = TRUE
                              )
                        
                  } else if (mcmc_kernel$method == "mvn_g_adaptive") {
                        adaptation_scale_record[param_rec_ind]   <- proposal_scaling
                        adaptation_shape_record[, , param_rec_ind] <-
                              kernel_cov
                        
                        if (messages &&
                            iter < stop_adaptation)
                              cat(
                                    paste0("Iteration: ", iter),
                                    paste0("Global acceptances: ", acceptances_g),
                                    paste0(
                                          "Global acceptance rate: ",
                                          acceptances_g / (iter - 1)
                                    ),
                                    file = status_file,
                                    sep = "\n",
                                    append = TRUE
                              )
                        
                  } else if (mcmc_kernel$method == "pcm_adaptive") {
                        adaptation_scale_record[, param_rec_ind] <- proposal_scaling
                        eigenvalue_record[, param_rec_ind]        <-
                              eigenvalues
                        eigenvector_record[, , param_rec_ind]      <-
                              eigenvectors
                        
                        if (messages &&
                            iter < stop_adaptation)
                              cat(
                                    paste0("Iteration: ", iter),
                                    paste0(
                                          "Directional acceptances: ",
                                          acceptances_c
                                    ),
                                    paste0(
                                          "Directional acceptance rates: ",
                                          acceptances_c / (iter - 1)
                                    ),
                                    file = status_file,
                                    sep = "\n",
                                    append = TRUE
                              )
                  } else if(mcmc_kernel$method == "afss") {
                        
                        slice_factor_record[, , param_rec_ind] <- slice_factors
                        slice_singval_record[, param_rec_ind]  <- slice_singvals
                        
                        if(messages) {
                              cat(paste0("Iteration: ", iter),
                                  file = status_file,
                                  sep = "\n",
                                  append = T)
                        }
                  }
                  
                  # Increment the parameter record index
                  param_rec_ind <- param_rec_ind + 1
            }
      }
      end.time <- Sys.time()
      
      # compile the results
      MCMC_results <- cbind(
            data_log_lik       = data_log_lik,
            lna_log_lik        = lna_log_lik,
            params_log_prior   = params_log_prior
      )
      rownames(MCMC_results) <- seq(1, iterations + 1, by = thin_params) - 1
      
      if (!fixed_inits)
            MCMC_results <- cbind(MCMC_results, initdist_log_prior = initdist_log_prior)
      if (!t0_fixed)
            MCMC_results <- cbind(MCMC_results, t0_log_prior = t0_log_prior)
      if (!is.null(tparam))
            MCMC_results <- cbind(MCMC_results, tparam_log_lik)
      
      # append the parameter samples on their natural and estimation scales
      MCMC_results <-
            as.data.frame(
                  cbind(MCMC_results,
                        parameter_samples_nat,
                        parameter_samples_est))
      
      # set the parameters (for restart) and save the results
      stem_object$dynamics$parameters <- model_params_nat
      if (!t0_fixed) stem_object$dynamics$t0 <- t0
      if (!fixed_inits) {
            stem_object$dynamics$initdist_params <- init_volumes_cur
            names(stem_object$dynamics$initdist_params) <- initdist_names
      }
      
      if (!is.null(tparam)) {
            stem_object$dynamics$tparam <- tparam
      }
      
      stem_object$results <-
            list(
                  time         = difftime(end.time, start.time, units = "hours"),
                  lna_paths    = lna_paths,
                  lna_draws    = lna_draws,
                  MCMC_results = MCMC_results,
                  ess_record   = ess_record
            )
      
      if (!is.null(tparam)) {
            stem_object$results$tparam_samples <- tparam_samples
            if (tparam_update == "block")
                  stem_object$results$tparam_ess_record <- tparam_ess_record
      }
      
      if (mcmc_kernel$method == "c_rw") {
            stem_object$results$acceptances_c = acceptances_c
            
      } else if (mcmc_kernel$method == "mvn_rw") {
            stem_object$results$acceptances_g = acceptances_g
            
      } else if (mcmc_kernel$method == "c_rw_adaptive") {
            stem_object$results$acceptances_c     = acceptances_c
            stem_object$results$adaptation_record =
                  list(
                        adaptation_scale_record  = t(adaptation_scale_record),
                        adaptation_shape_record  = adaptation_shape_record,
                        proposal_covariance      =
                              diag(sqrt(proposal_scaling)) %*% kernel_cov %*% diag(sqrt(proposal_scaling))
                  )
            
      } else if (mcmc_kernel$method == "mvn_c_adaptive") {
            stem_object$results$acceptances_g     = acceptances_g
            stem_object$results$adaptation_record =
                  list(
                        adaptation_scale_record  = t(adaptation_scale_record),
                        adaptation_shape_record  = adaptation_shape_record,
                        proposal_covariance      =
                              diag(sqrt(proposal_scaling)) %*% kernel_cov %*% diag(sqrt(proposal_scaling))
                  )
            
      } else if (mcmc_kernel$method == "mvn_g_adaptive") {
            stem_object$results$acceptances_g     = acceptances_g
            stem_object$results$adaptation_record =
                  list(
                        adaptation_scale_record  = adaptation_scale_record,
                        adaptation_shape_record  = adaptation_shape_record,
                        proposal_covariance      = proposal_scaling * kernel_cov
                  )
            
      } else if (mcmc_kernel$method == "pcm_adaptive") {
            stem_object$results$acceptances_c     = acceptances_c
            stem_object$results$proposals_c       = proposals_c
            stem_object$results$adaptation_record =
                  list(
                        adaptation_scale_record  = t(adaptation_scale_record),
                        eigenvalue_record        = t(eigenvalue_record),
                        eigenvector_record       = eigenvector_record,
                        proposal_covariance      = eigenvectors %*% diag(proposal_scaling * eigenvalues) %*% t(eigenvectors)
                  )
      } else if (mcmc_kernel$method == "afss") {
            stem_object$results$adaptation_record = 
                  list(
                        slice_factor_record   = slice_factor_record,
                        slice_singval_record  = t(slice_singval_record),
                        proposal_covariance   = kernel_cov
                  )
      }
      
      if (!fixed_inits || !t0_fixed) {
            stem_object$results$acceptances_init = acceptances_init
      }
      
      # save the settings
      stem_object$stem_settings <-
            list(
                  iterations       = iterations,
                  thin_params      = thin_params,
                  thin_latent_proc = thin_latent_proc,
                  n_ess_updates    = n_ess_updates,
                  priors           = priors,
                  prior_density    = prior_density,
                  mcmc_kernel      = mcmc_kernel,
                  t0_kernel        = t0_kernel,
                  initdist_sampler = initdist_sampler,
                  path_for_restart = path,
                  tparam_for_restart = tparam
            )
      
      return(stem_object)
}
