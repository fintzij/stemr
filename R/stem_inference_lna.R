                        census_lna(
                              path                = pathmat_prop,
                              census_path         = censusmat,
                              census_inds         = census_indices,
                              lna_event_inds      = lna_event_inds,
                              flow_matrix_lna     = flow_matrix,
                              do_prevalence       = do_prevalence,
                              init_state          = init_volumes_cur,
                              forcing_matrix      = forcing_matrix
                        )
                        
                        # evaluate the density of the incidence counts
                        evaluate_d_measure_LNA(
                              emitmat           = emitmat,
                              obsmat            = data,
                              censusmat         = censusmat,
                              measproc_indmat   = measproc_indmat,
                              lna_parameters    = lna_params_prop,
                              lna_param_inds    = lna_param_inds,
                              lna_const_inds    = lna_const_inds,
                              lna_tcovar_inds   = lna_tcovar_inds,
                              param_update_inds = param_update_inds,
                              census_indices    = census_indices,
                              lna_param_vec     = lna_param_vec,
                              d_meas_ptr        = d_meas_pointer
                        )
                        
                        # compute the data log likelihood
                        data_log_lik_prop <- sum(emitmat[, -1][measproc_indmat])
                        if (is.nan(data_log_lik_prop)) data_log_lik_prop <- -Inf
                  }, silent = TRUE)
                  
                  if (is.null(data_log_lik_prop))
                        data_log_lik_prop <- -Inf
                  
                  # compute the log posterior for the proposed parameters
                  logpost_prop <-
                        data_log_lik_prop + params_logprior_prop
                  
                  ## Compute the acceptance probability
                  acceptance_prob <- logpost_prop - logpost_cur
                  
                  # Accept/Reject via metropolis-hastings
                  if (acceptance_prob >= 0 || acceptance_prob >= log(runif(1))) {
                        ### ACCEPTANCE
                        acceptances_g       <-
                              acceptances_g + 1    # increment acceptances
                        
                        copy_vec(path$data_log_lik,
                                 data_log_lik_prop)      # update the data log likelihood
                        copy_vec(params_logprior_cur,
                                 params_logprior_prop) # update the prior density
                        copy_vec(logpost_cur, logpost_prop)                 # update the log posterior
                        
                        copy_mat(lna_params_cur, lna_params_prop)   # update the model parameter
                        copy_vec(model_params_nat, params_prop_nat) # update LNA parameters on their natural scales
                        copy_vec(model_params_est, params_prop_est) # update LNA parameters on their estimation scales
                  }
                  
                  if (iter < stop_adaptation) {
                        # Adapt the proposal kernel
                        proposal_scaling <-
                              min(exp(
                                    log(proposal_scaling) +
                                          adaptations[iter] * (min(
                                                exp(acceptance_prob), 1
                                          ) - target_g)
                              ),
                              max_scaling)
                        
                        kernel_resid <-
                              model_params_est - kernel_mean
                        kernel_cov   <-
                              kernel_cov + adaptations[iter] * (kernel_resid %*% t(kernel_resid) - kernel_cov)
                        kernel_mean  <-
                              kernel_mean + adaptations[iter] * kernel_resid
                  }
            } else if (mcmc_kernel$method == "pcm_adaptive") {
                  if (iter == stop_adaptation) {
                        mcmc_kernel$sigma = eigenvectors %*% diag(proposal_scaling * eigenvalues) %*% t(eigenvectors)
                  }
                  
                  # sample either a single direction or establish the sequence of directions
                  if (mcmc_kernel$kernel_settings$pcm_setting_list$pcm_update_schedule == "random") {
                        d_schedule <-
                              sample.int(
                                    n_model_params,
                                    n_pcm_updates,
                                    replace = FALSE,
                                    prob = comp_weights
                              )
                  }
                  
                  # propose new parameters
                  for (direction in d_schedule) {
                        # sample a direction
                        proposals_c[direction] <-
                              proposals_c[direction] + 1
                        
                        # propose new parameters
                        pcm_adaptive(
                              params_prop      = params_prop_est,
                              params_cur       = model_params_est,
                              eigenvalues      = eigenvalues,
                              eigenvectors     = eigenvectors,
                              proposal_scaling = proposal_scaling,
                              direction        = direction - 1,
                              nugget           = nugget
                        )
                        
                        # Convert the proposed parameters to their natural scale
                        params_prop_nat <-
                              from_estimation_scale(params_prop_est)
                        
                        # update time-varying parameters if necessary
                        if (!is.null(tparam)) {
                              for (p in seq_along(tparam)) {
                                    insert_tparam(
                                          tcovar    = lna_params_prop,
                                          values    = tparam[[p]]$draws2par(
                                                parameters = params_prop_nat,
                                                draws = tparam[[p]]$draws_cur
                                          ),
                                          col_ind   = tparam[[p]]$col_ind,
                                          tpar_inds = tparam[[p]]$tpar_inds
                                    )
                              }
                        }
                        
                        # Compute the log prior for the proposed parameters
                        params_logprior_prop <-
                              prior_density(params_prop_nat, params_prop_est)
                        
                        # Insert the proposed parameters into the parameter proposal matrix
                        pars2lnapars(lna_params_prop,
                                     c(
                                           params_prop_nat,
                                           t0,
                                           init_volumes_cur
                                     ))
                        
                        # set the data log likelihood for the proposal to NULL
                        data_log_lik_prop <- NULL
                        
                        try({
                              map_draws_2_lna(
                                    pathmat           = pathmat_prop,
                                    draws             = path$draws,
                                    lna_times         = lna_times,
                                    lna_pars          = lna_params_prop,
                                    lna_param_vec     = lna_param_vec,
                                    lna_param_inds    = lna_param_inds,
                                    lna_tcovar_inds   = lna_tcovar_inds,
                                    init_start        = lna_initdist_inds[1],
                                    param_update_inds = param_update_inds,
                                    stoich_matrix     = stoich_matrix,
                                    forcing_inds      = forcing_inds,
                                    forcing_matrix    = forcing_matrix,
                                    svd_sqrt          = svd_sqrt,
                                    svd_d             = svd_d,
                                    svd_U             = svd_U,
                                    svd_V             = svd_V,
                                    lna_pointer       = lna_pointer,
                                    set_pars_pointer  = lna_set_pars_pointer,
                                    step_size         = step_size
                              )
                              
                              census_lna(
                                    path                = pathmat_prop,
                                    census_path         = censusmat,
                                    census_inds         = census_indices,
                                    lna_event_inds      = lna_event_inds,
                                    flow_matrix_lna     = flow_matrix,
                                    do_prevalence       = do_prevalence,
                                    init_state          = init_volumes_cur,
                                    forcing_matrix      = forcing_matrix
                              )
                              
                              # evaluate the density of the incidence counts
                              evaluate_d_measure_LNA(
                                    emitmat           = emitmat,
                                    obsmat            = data,
                                    censusmat         = censusmat,
                                    measproc_indmat   = measproc_indmat,
                                    lna_parameters    = lna_params_prop,
                                    lna_param_inds    = lna_param_inds,
                                    lna_const_inds    = lna_const_inds,
                                    lna_tcovar_inds   = lna_tcovar_inds,
                                    param_update_inds = param_update_inds,
                                    census_indices    = census_indices,
                                    lna_param_vec     = lna_param_vec,
                                    d_meas_ptr        = d_meas_pointer
                              )
                              
                              # compute the data log likelihood
                              data_log_lik_prop <-
                                    sum(emitmat[, -1][measproc_indmat])
                              if (is.nan(data_log_lik_prop))
                                    data_log_lik_prop <- -Inf
                        }, silent = TRUE)
                        
                        if (is.null(data_log_lik_prop))
                              data_log_lik_prop <- -Inf
                        
                        # compute the log posterior for the proposed parameters
                        logpost_prop <- data_log_lik_prop + params_logprior_prop
                        
                        ## Compute the acceptance probability
                        acceptance_prob <- logpost_prop - logpost_cur
                        
                        # Accept/Reject via metropolis-hastings
                        if (acceptance_prob >= 0 || acceptance_prob >= log(runif(1))) {
                              ### ACCEPTANCE
                              acceptances_c[direction] <-
                                    acceptances_c[direction] + 1    # increment acceptances
                              
                              copy_vec(path$data_log_lik,
                                       data_log_lik_prop)      # update the data log likelihood
                              copy_vec(params_logprior_cur,
                                       params_logprior_prop) # update the prior density
                              copy_vec(logpost_cur, logpost_prop)                 # update the log posterior
                              
                              copy_mat(lna_params_cur,
                                       lna_params_prop)   # update the model parameter
                              copy_vec(model_params_nat,
                                       params_prop_nat) # update LNA parameters on their natural scales
                              copy_vec(model_params_est,
                                       params_prop_est) # update LNA parameters on their estimation scales
                        }
                        
                        if (iter < stop_adaptation) {
                              # Adapt the direction scaling
                              proposal_scaling[direction] <-
                                    min(
                                          exp(
                                                log(proposal_scaling[direction]) +
                                                      adaptations[iter] * (
                                                            min(exp(
                                                                  acceptance_prob
                                                            ), 1) - acceptances_c[direction] / proposals_c[direction]
                                                      ) +
                                                      adaptations[iter] * (
                                                            acceptances_c[direction] / proposals_c[direction] - target_c
                                                      )
                                          ),
                                          max_scaling
                                    )
                        }
                  }
                  
                  # update the covariance matrix for the proposal kernel
                  if (iter < stop_adaptation) {
                        kernel_resid <- model_params_est - kernel_mean
                        kernel_cov   <-
                              kernel_cov + adaptations[iter] * (kernel_resid %*% t(kernel_resid) - kernel_cov)
                        kernel_mean  <-
                              kernel_mean + adaptations[iter] * kernel_resid
                        
                        update_princomps(
                              eigenvalues    = eigenvalues,
                              eigenvectors   = eigenvectors,
                              comp_weights   = comp_weights,
                              kernel_cov     = kernel_cov,
                              update_weights = (iter - 1) %% weight_update_interval == 0
                        )
                  }
                  
            } else if (mcmc_kernel$method == "afss") {
                  
                  if (iter == stop_adaptation) {
                        mcmc_kernel$sigma = kernel_cov
                        mcmc_kernel$kernel_settings$afss_setting_list = 
                              afss_settings(
                                    factor_update_interval = factor_update_interval,
                                    prob_update_interval   = prob_update_interval,
                                    first_factor_update    = first_factor_update,
                                    first_prob_update      = first_prob_update,
                                    initial_widths         = interval_widths,
                                    initial_factors        = slice_factors,
                                    initial_slice_probs    = slice_probs
                              )
                        
                        if(!is.null(factor_update_interval_fcn)) 
                              mcmc_kernel$kernel_settings$afss_setting_list$factor_update_interval = 
                              factor_update_interval_fcn()
                        
                        if(!is.null(prob_update_interval_fcn))
                              mcmc_kernel$kernel_settings$afss_setting_list$prob_update_interval = 
                              prob_update_interval_fcn()
                  }
                  
                  factor_slice_sampler(
                        model_params_est     = model_params_est,
                        model_params_nat     = model_params_nat,
                        params_prop_est      = params_prop_est,
                        params_prop_nat      = params_prop_nat,
                        interval_widths      = interval_widths,
                        slice_factors        = slice_factors,
                        nugget               = nugget,
                        n_expansions         = n_expansions,
                        n_contractions       = n_contractions,
                        n_expansions_c       = n_expansions_c,
                        n_contractions_c     = n_contractions_c,
                        slice_probs          = slice_probs,
                        min_afss_updates     = min_afss_updates,
                        path                 = path,
                        data                 = data,
                        priors               = priors,
                        params_logprior_cur  = params_logprior_cur,
                        logpost_cur          = logpost_cur,
                        lna_params_cur       = lna_params_cur,
                        lna_param_vec        = lna_param_vec,
                        tparam               = tparam,
                        censusmat            = censusmat,
                        emitmat              = emitmat,
                        flow_matrix          = flow_matrix,
                        stoich_matrix        = stoich_matrix,
                        lna_times            = lna_times,
                        forcing_inds         = forcing_inds,
                        forcing_matrix       = forcing_matrix,
                        lna_param_inds       = lna_param_inds,
                        lna_const_inds       = lna_const_inds,
                        lna_tcovar_inds      = lna_tcovar_inds,
                        lna_initdist_inds    = lna_initdist_inds,
                        param_update_inds    = param_update_inds,
                        lna_event_inds       = lna_event_inds,
                        census_indices       = census_indices,
                        measproc_indmat      = measproc_indmat,
                        svd_sqrt             = svd_sqrt,
                        svd_d                = svd_d,
                        svd_U                = svd_U,
                        svd_V                = svd_V,
                        lna_pointer          = lna_pointer,
                        lna_set_pars_pointer = lna_set_pars_pointer,
                        d_meas_pointer       = d_meas_pointer,
                        do_prevalence        = do_prevalence,
                        step_size            = step_size
                  )
                  
                  # update the interval widths
                  update_interval_widths(
                        interval_widths   = interval_widths,
                        n_expansions      = n_expansions,
                        n_contractions    = n_contractions,
                        n_expansions_c    = n_expansions_c,
                        n_contractions_c  = n_contractions_c,
                        slice_ratios      = slice_ratios,
                        adaptation_factor = adaptations[width_adaptation_ind]
                  )
                  
                  # increment the width adaptation index
                  width_adaptation_ind <- width_adaptation_ind + 1
                  
                  # update the covariance matrix for the proposal kernel
                  if (iter < stop_adaptation) {
                        
                        kernel_resid <- model_params_est - kernel_mean
                        kernel_cov   <- kernel_cov + adaptations[iter] * (kernel_resid %*% t(kernel_resid) - kernel_cov)
                        kernel_mean  <- kernel_mean + adaptations[iter] * kernel_resid
                        
                        if (((iter-1) >= first_factor_update) && 
                            ((iter-1) %% factor_update_interval == 0)) {
                              
                              # update the slice directions
                              if(use_cov) {
                                    update_factors(
                                          slice_singvals  = slice_singvals,
                                          slice_factors   = slice_factors,
                                          slice_factors_t = slice_factors_t,
                                          kernel_cov      = kernel_cov
                                    )
                              } else {
                                    update_factors(
                                          slice_singvals  = slice_singvals,
                                          slice_factors   = slice_factors,
                                          slice_factors_t = slice_factors_t,
                                          kernel_cov      = cov2cor(kernel_cov)
                                    )
                              }
                              
                              # increment the factor update interval
                              if(!is.null(factor_update_interval_fcn)) {
                                    factor_update_interval <- factor_update_interval_fcn(factor_update_interval)
                              }
                              
                              # reset the interval widths (new widths an average of 1 and the previous width)
                              if(factor_update_interval != 1 || iter == first_factor_update) {
                                    reset_slice_ratios(n_expansions     = n_expansions,
                                                       n_contractions   = n_contractions,
                                                       n_expansions_c   = n_expansions_c,
                                                       n_contractions_c = n_contractions_c,
                                                       slice_ratios     = slice_ratios)
                                    
                                    # reset the interval width adaptation index
                                    width_adaptation_ind <- 2
                              }
                        }
                        
                        # adapt the slice probabilities
                        if(((iter-1) >= first_prob_update) &&
                           ((iter-1) %% prob_update_interval == 0)) {
                              
                              copy_vec(dest = slice_probs, 
                                       orig = slice_singvals ^ 0.5 / sum(slice_singvals ^ 0.5))
                              
                              if(!is.null(prob_update_interval_fcn)) {
                                    prob_update_interval <- prob_update_interval_fcn(prob_update_interval)
                              }
                        }
                  }
            }
            
            # Propose and Accept-reject initial state/time
            if (!fixed_inits || !t0_fixed) {
                  # if t0 is not fixed, sample a new value
                  if (!t0_fixed) {
                        # sample the new time from proposal centered at t0
                        t0_prop <-
                              extraDistr:::cpp_rtnorm(
                                    n     = 1,
                                    mu    = t0,
                                    sigma = t0_kernel$rw_sd,
                                    lower = t0_kernel$lower,
                                    upper = t0_kernel$upper
                              )
                        
                        # log prior of the proposal
                        t0_logprior_prop <-
                              extraDistr:::cpp_dtnorm(
                                    x        = t0_prop,
                                    mu       = t0_kernel$mean,
                                    sigma    = t0_kernel$sd,
                                    lower    = t0_kernel$lower,
                                    upper    = t0_kernel$upper,
                                    log_prob = TRUE
                              )
                        # insert the new time
                        lna_times[1]       <- t0_prop
                        path$lna_path[1, 1] <- t0_prop
                        pathmat_prop[1, 1]  <- t0_prop
                        
                        ### compute the log proposal probabilities for the forward and reverse moves
                        # prob of going from current to new t0
                        t0_cur2new <-
                              extraDistr:::cpp_dtnorm(
                                    x         = t0_prop,
                                    mu        = t0,
                                    sigma     = t0_kernel$rw_sd,
                                    lower     = t0_kernel$lower,
                                    upper     = t0_kernel$upper,
                                    log_prob  = TRUE
                              )
                        
                        # prob of going from new to current t0
                        t0_new2cur <-
                              extraDistr:::cpp_dtnorm(
                                    x        = t0,
                                    mu       = t0_prop,
                                    sigma    = t0_kernel$rw_sd,
                                    lower    = t0_kernel$lower,
                                    upper    = t0_kernel$upper,
                                    log_prob = TRUE
                              )
                  }
                  
                  # If the initial compartment counts are not fixed, sample new values
                  if (!fixed_inits) {
                        initdist_params_prop <- initdist_sampler()
                        init_volumes_prop    <-
                              concs2vols(initdist_params_prop)
                  }
                  
                  # Insert the proposed parameters into the parameter proposal matrix
                  pars2lnapars(lna_params_prop, c(model_params_nat, t0_prop, init_volumes_prop))
                  
                  # set the data log likelihood for the proposal to NULL
                  data_log_lik_prop <- NULL
                  
                  try({
                        map_draws_2_lna(
                              pathmat           = pathmat_prop,
                              draws             = path$draws,
                              lna_times         = lna_times,
                              lna_pars          = lna_params_prop,
                              lna_param_vec     = lna_param_vec,
                              lna_param_inds    = lna_param_inds,
                              lna_tcovar_inds   = lna_tcovar_inds,
                              init_start        = lna_initdist_inds[1],
                              param_update_inds = param_update_inds,
                              stoich_matrix     = stoich_matrix,
                              forcing_inds      = forcing_inds,
                              forcing_matrix    = forcing_matrix,
                              svd_sqrt          = svd_sqrt,
                              svd_d             = svd_d,
                              svd_U             = svd_U,
                              svd_V             = svd_V,
                              lna_pointer       = lna_pointer,
                              set_pars_pointer  = lna_set_pars_pointer,
                              step_size         = step_size
                        )
                        
                        census_lna(
                              path                = pathmat_prop,
                              census_path         = censusmat,
                              census_inds         = census_indices,
                              lna_event_inds      = lna_event_inds,
                              flow_matrix_lna     = flow_matrix,
                              do_prevalence       = do_prevalence,
                              init_state          = init_volumes_prop,
                              forcing_matrix      = forcing_matrix
                        )
                        
                        # evaluate the density of the incidence counts
                        evaluate_d_measure_LNA(
                              emitmat           = emitmat,
                              obsmat            = data,
                              censusmat         = censusmat,
                              measproc_indmat   = measproc_indmat,
                              lna_parameters    = lna_params_prop,
                              lna_param_inds    = lna_param_inds,
                              lna_const_inds    = lna_const_inds,
                              lna_tcovar_inds   = lna_tcovar_inds,
                              param_update_inds = param_update_inds,
                              census_indices    = census_indices,
                              lna_param_vec     = lna_param_vec,
                              d_meas_ptr        = d_meas_pointer
                        )
                        
                        # compute the data log likelihood
                        data_log_lik_prop <- sum(emitmat[, -1][measproc_indmat])
                        if (is.nan(data_log_lik_prop)) data_log_lik_prop <- -Inf
                  }, silent = TRUE)
                  
                  if (is.null(data_log_lik_prop)) data_log_lik_prop <- -Inf
                  
                  ## Compute the log posteriors
                  # N.B. no need to include the initial distribution log likelihoods
                  # since those are updated via an independence sampler so they cancel out
                  acceptance_prob <- data_log_lik_prop - path$data_log_lik
                  
                  # if t0 is not fixed, need to include the proposal probabilities in the MH ratio
                  if (!t0_fixed) acceptance_prob <- acceptance_prob + t0_logprior_prop - t0_logprior_cur + t0_new2cur - t0_cur2new
                  
                  # Accept/Reject via metropolis-hastings
                  if (acceptance_prob >= 0 || acceptance_prob >= log(runif(1))) {
                        
                        ### ACCEPTANCE
                        acceptances_init  <- acceptances_init + 1      # increment acceptances
                        copy_vec(path$data_log_lik, data_log_lik_prop) # update the data log likelihood
                        copy_vec(logpost_cur, path$data_log_lik + params_logprior_cur) # update log posterior
                        copy_mat(lna_params_cur, lna_params_prop)      # update LNA parameter matrix
                        
                        # Update the initial distribution parameters and log prior if not fixed
                        if (!fixed_inits) {
                              copy_vec(initdist_params_cur, initdist_params_prop)
                              copy_vec(init_volumes_cur, init_volumes_prop)
                        }
                        
                        # update t0 and its log prior if it is not fixed
                        if (!t0_fixed) {
                              t0              <- t0_prop              # update t0
                              t0_logprior_cur <- t0_logprior_prop     # update the log prior for t0
                        }
                        
                  } else {
                        ### REJECTION - only need to reset t0 if it is not fixed
                        if (!t0_fixed) {
                              lna_times[1] <- t0
                              path$lna_path[1, 1] <- t0
                              pathmat_prop[1, 1] <- t0
                        }
                  }
            }
            
            # update the tparam draws
            if (!is.null(tparam) && tparam_update == "block") {
                  update_tparam(
                        tparam               = tparam,
                        path_cur             = path,
                        data                 = data,
                        lna_parameters       = lna_params_cur,
                        lna_param_vec        = lna_param_vec,
                        pathmat_prop         = pathmat_prop,
                        censusmat            = censusmat,
                        emitmat              = emitmat,
                        flow_matrix          = flow_matrix,
                        stoich_matrix        = stoich_matrix,
                        lna_times            = lna_times,
                        forcing_inds         = forcing_inds,
                        forcing_matrix       = forcing_matrix,
                        lna_param_inds       = lna_param_inds,
                        lna_const_inds       = lna_const_inds,
                        lna_tcovar_inds      = lna_tcovar_inds,
                        lna_initdist_inds    = lna_initdist_inds,
                        param_update_inds    = param_update_inds,
                        census_indices       = census_indices,
                        lna_event_inds       = lna_event_inds,
                        measproc_indmat      = measproc_indmat,
                        svd_sqrt             = svd_sqrt,
                        svd_d                = svd_d,
                        svd_U                = svd_U,
                        svd_V                = svd_V,
                        lna_pointer          = lna_pointer,
                        lna_set_pars_pointer = lna_set_pars_pointer,
